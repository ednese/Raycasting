#include "../cub3d.h"

int		ft_strdif(char *s, char c1, char c2, char c3)
{
	int i;

	i = 0;
	if (s == 0)
		return (0);
	while (s[i])
	{
		if (s[i] != c1 && s[i] != c2 && s[i] != c3)
			return (1);
		i++;
	}
	return (0);
}
