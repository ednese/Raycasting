#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int	nbr;

	nbr = n;
	if (n < 0)
	{
		nbr *= -1;
		ft_putchar('-');
	}
	if (nbr > 10)
		ft_putnbr(nbr / 10);
	ft_putchar(nbr % 10 + 48);
}
