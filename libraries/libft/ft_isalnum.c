#include "libft.h"

int	ft_isalnum(int c)
{
	int cpt;

	cpt = 0;
	if (c >= 97 && c <= 122)
		cpt++;
	if (c >= 65 && c <= 90)
		cpt++;
	if (c >= 48 && c <= 57)
		cpt++;
	if (cpt > 0)
		return (1);
	return (0);
}
